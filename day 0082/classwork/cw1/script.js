//  ამ ფუნქციას ექნება 1 პარამეტი სახელად border (ზღვარი)
//   ფუნქციამ 0-იდან border-ის ჩათვლით უნდა გაიგოს ყველა რიცხვი
//    if პირობითი განცხადების საშვალებით,
//  ეს ყველა ლუწი რიცხვი უნდა შევკრიბოთ და დავაბრუნოთ

function evenSum(border) {
    let numSum = 0
    for (let i = 0; i <= border; i ++) {
        if (i % 2 === 0) {
            numSum = numSum + i
        }
        
    }
    document.write(numSum)
}